let scannerStarted = false;

function loadScanHistory() {
  document.getElementById('scan-history-tbody').innerHTML = EY.scanHistory.slice(0,6).map(s => `
    <tr>
      <td><span style="font-family:monospace;font-size:11px">${s.code}</span></td>
      <td>${catBadge(s.category)}</td>
      <td>${s.weight}</td>
      <td>${s.by}</td>
      <td><span class="badge ${s.action==='DISPOSED'?'s-neutral':s.action==='GENERATED'?'s-info':'s-ok'}">${s.action}</span></td>
      <td>${s.time}</td>
      <td>${statusBadge(s.action === 'DISPOSED' ? 'DISPOSED' : s.action === 'GENERATED' ? 'GENERATED' : 'COLLECTED')}</td>
    </tr>
  `).join('');
}

